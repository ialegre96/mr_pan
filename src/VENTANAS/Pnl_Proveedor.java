    private void limpiarTabla(){
        //Limpiar Tabla
                    int elimTotal = tblProveedor.getRowCount();
                    for (int i = elimTotal-1; i >= 0; i--) {
                        modelo.removeRow(i);
                    }
    }
    
    private void limpiarCampos(){
      txtProveedor_ID.setText("");
      txtNombre.setText("");
      txtDireccion.setText("");
      txtDistrito.setText("");
      txtCorreo.setText(""); 
      
      limpiarTabla();
      mostrar();
      
      txtProveedor_ID.grabFocus();
    }
    
    private void mostrar(){
        Proveedor p = new Proveedor();

        try {
            List<Proveedor> proveedores = p.obtenerTodos();

            //Iterator<Cliente> it = Menu.listaCliente.iterator();

            Iterator<Proveedor> it = proveedores.iterator();

            //Mostrar Datos Almacenados previamente
            while(it.hasNext()){
                p = it.next();
                String agregar[] = new String[5];
                agregar[0] = p.getID_Proveedor();
                agregar[1] = p.getNomProveedor();
                agregar[2] = p.getDireccion();
                agregar[3] = p.getDistrito();
                agregar[4] = p.getCorreo();

                modelo.addRow(agregar);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Pnl_Cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
